int test_move(unsigned short xi, unsigned short yi, char *xpm[], 
				unsigned short hor, short delta, unsigned short time) {
	int ipc_status, irq_set_timer, irq_set_kbd, r, proceed = 1;
	message msg;
	unsigned long count_ticks = 0;
	unsigned long pixels_per_tick = (((delta) / time) / 60) * 1000;//float was not working, so improvisation had to
	printf("(%d / %d ) * 60 = %d \n", delta, time, pixels_per_tick);

	void * v_mem = vg_init(VIDEO_MODE);
	Sprite * s = create_sprite(xpm, xi, yi);

	if(s == NULL){
		vg_exit();
		return 1;
	}
	vg_apply();

	if((irq_set_timer = timer_subscribe_int()) == -1){
		printf("unable to subscribe timer\n");
		return 1;
	}
	if((irq_set_kbd = kbd_subscribe_int()) == -1){
		printf("unable to subscribe kbd\n");
		return 1;
	}

	while(proceed){//(count_ticks/60 < time && proceed) {
		if ( (r = driver_receive(ANY, &msg, &ipc_status)) != 0 ) {
			printf("driver_receive failed with: %d", r);
			continue;
		}
		if (is_ipc_notify(ipc_status)) {
			switch (_ENDPOINT_P(msg.m_source)) {
			case HARDWARE:
				if (msg.NOTIFY_ARG & irq_set_timer) {
					count_ticks++;
					if(count_ticks/60 < time){
						vg_move_pixmap(s->x, s->y, s->map, s->width, s->height, pixels_per_tick, count_ticks, hor);
						//vg_pixel(20 ,50+ count_ticks, 0xf);
						vg_apply();
					}
				}else if(msg.NOTIFY_ARG & irq_set_kbd){
					if(kbd_int_handler() == ESC_KEY){
						proceed = 0;
					}//print scancodes
				}
				break;
			default:
				break; /* no other notifications expected: do nothing */
			}
		}
	}
	if (timer_unsubscribe_int() != 0){
		printf("unable to unsubscribe timer\n");
		return 1;
	}
	if (kbd_unsubscribe_int() != 0){
		printf("unable to unsubscribe kbd\n");
		return 1;
	}

	if(vg_exit()!=0){
		return 1;
	}
	return 0;

}					

